// Current language state
let currentLanguage: Language = 'en';

// Initialize language from localStorage or browser preference
export const initializeLanguage = (): void => {
  try {
    // Try to get from localStorage first
    const savedLanguage = localStorage.getItem('ebook-reader-language') as Language;
    if (savedLanguage && AVAILABLE_LANGUAGES.some(lang => lang.code === savedLanguage)) {
      currentLanguage = savedLanguage;
      return;
    }

    // Fallback to browser language
    const browserLanguage = navigator.language.split('-')[0] as Language;
    if (AVAILABLE_LANGUAGES.some(lang => lang.code === browserLanguage)) {
      currentLanguage = browserLanguage;
    } else {
      currentLanguage = 'en'; // Default fallback
    }
  } catch (error) {
    console.warn('Error initializing language:', error);
    currentLanguage = 'en';
  }
};

// Get current language
export const getCurrentLanguage = (): Language => {
  return currentLanguage;
};

// Set language and persist to localStorage
export const setLanguage = (language: Language): void => {
  if (AVAILABLE_LANGUAGES.some(lang => lang.code === language)) {
    currentLanguage = language;
    try {
      localStorage.setItem('ebook-reader-language', language);
    } catch (error) {
      console.warn('Error saving language to localStorage:', error);
    }
  } else {
    console.warn(`Unsupported language: ${language}`);
  }
};

// Get available languages
export const getAvailableLanguages = (): LanguageInfo[] => {
  return AVAILABLE_LANGUAGES;
};

// Get translation for a key
export const getTranslation = (key: keyof TranslationKeys): string => {
  const translations = TRANSLATIONS[currentLanguage];
  
  if (!translations) {
    console.warn(`No translations found for language: ${currentLanguage}`);
    return TRANSLATIONS.en[key] || key;
  }

  const translation = translations[key];
  
  if (!translation) {
    console.warn(`Missing translation for key: ${key} in language: ${currentLanguage}`);
    return TRANSLATIONS.en[key] || key;
  }

  return translation;
};

// Get translation with interpolation support
export const getTranslationWithParams = (key: keyof TranslationKeys, params: Record<string, string | number>): string => {
  let translation = getTranslation(key);
  
  // Replace parameters in the translation
  Object.entries(params).forEach(([param, value]) => {
    const placeholder = `{${param}}`;
    translation = translation.replace(new RegExp(placeholder, 'g'), String(value));
  });
  
  return translation;
};

// Check if a translation exists
export const hasTranslation = (key: keyof TranslationKeys, language?: Language): boolean => {
  const lang = language || currentLanguage;
  const translations = TRANSLATIONS[lang];
  return translations && translations[key] !== undefined;
};

// Get language direction (for future RTL support)
export const getLanguageDirection = (language?: Language): 'ltr' | 'rtl' => {
  const lang = language || currentLanguage;
  // Add RTL languages here when supported
  const rtlLanguages: Language[] = [];
  return rtlLanguages.includes(lang) ? 'rtl' : 'ltr';
};

